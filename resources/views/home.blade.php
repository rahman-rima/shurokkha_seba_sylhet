        <section class="About" id="About">
            <h2>
                কোভিড-১৯ সম্পর্কিত
            </h2>

            <div class="AboutCV">

                <br><p>
                    করোনাভাইরাস রোগ ২০১৯ বা কোভিড-১৯ মানুষের একটি সংক্রামক ব্যাধি যা গুরুতর তীব্র শ্বাসযন্ত্রীয় রোগলক্ষণসমষ্টি সৃষ্টিকারী করোনাভাইরাস ২ (সার্স-কোভ-২) নামক এক ধরনের ভাইরাসের আক্রমণে হয়ে থাকে। এই ব্যাধিটি সর্বপ্রথম ২০১৯ সালের ডিসেম্বর মাসে চীনে শনাক্ত করা হয়। পরবর্তীতে ২০২০ সালের প্রারম্ভে ব্যাধিটি বিশ্বব্যাপী ছড়িয়ে পড়ে এবং বৈশ্বিক মহামারীর রূপ ধারণ করে। ব্যাধিটির সাধারণ উপসর্গ হিসেবে জ্বর, সর্দি এবং শ্বাসকষ্ট দেখা যায়। কিছু ক্ষেত্রে মাংসপেশীর ব্যথা, বারবার থুতু সৃষ্টি এবং গলায় ব্যথা দেখা যেতে পারে। বেশিরভাগ ক্ষেত্রেই উপসর্গগুলো নমনীয় আকারে দেখা যায়, কিন্তু কিছু গুরুতর ক্ষেত্রে ফুসুফুস প্রদাহ (নিউমোনিয়া) এবং বিভিন্ন অঙ্গের বিকলতাও দেখা যায়।
